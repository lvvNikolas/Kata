// ============================================================
// robots.js — Автоматическая генерация robots.txt
// ============================================================
// Next.js App Router автоматически создаёт файл /robots.txt
// на основе данных которые возвращает эта функция.
//
// Зачем нужен robots.txt:
//   - Говорит поисковым роботам (Google, Bing и др.) что им
//     разрешено или запрещено индексировать на сайте
//   - userAgent: '*' = правило для ВСЕХ поисковиков
//   - allow: '/' = разрешить индексацию всего сайта
//
// Если нужно закрыть какую-то страницу от индексации:
//   disallow: ['/admin', '/private']
//
// Проверить результат: https://kata-es4q.vercel.app/robots.txt
// ============================================================

const BASE_URL = 'https://kata-es4q.vercel.app';

export default function robots() {
  return {
    rules: {
      userAgent: '*',   // Применяется ко всем поисковым роботам
      allow: '/',       // Разрешаем индексировать весь сайт
    },
    // Ссылка на sitemap — помогает Google найти все страницы
    sitemap: `${BASE_URL}/sitemap.xml`,
  };
}
